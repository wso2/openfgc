-- Consent Management API Database Schema (SQLite)

-- Main consent table
CREATE TABLE IF NOT EXISTS CONSENT (
  CONSENT_ID            CHAR(36) NOT NULL,
  CREATED_TIME          BIGINT NOT NULL,
  UPDATED_TIME          BIGINT NOT NULL,
  GROUP_ID              VARCHAR(255) NOT NULL,
  CONSENT_TYPE          VARCHAR(64) NOT NULL,
  CURRENT_STATUS        VARCHAR(64) NOT NULL,
  CONSENT_FREQUENCY     INT DEFAULT NULL,
  EXPIRATION_TIME       BIGINT DEFAULT NULL,
  RECURRING_INDICATOR   BOOLEAN DEFAULT NULL,
  DATA_ACCESS_VALIDITY_DURATION BIGINT DEFAULT NULL,
  ORG_ID                VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (CONSENT_ID)
);

CREATE INDEX IF NOT EXISTS idx_consent_group_id       ON CONSENT (GROUP_ID);
CREATE INDEX IF NOT EXISTS idx_consent_type           ON CONSENT (CONSENT_TYPE);
CREATE INDEX IF NOT EXISTS idx_consent_current_status ON CONSENT (CURRENT_STATUS);
CREATE INDEX IF NOT EXISTS idx_consent_created_time   ON CONSENT (CREATED_TIME);
CREATE INDEX IF NOT EXISTS idx_consent_updated_time   ON CONSENT (UPDATED_TIME);
CREATE INDEX IF NOT EXISTS idx_consent_org_id         ON CONSENT (ORG_ID);

-- Authorization resource table
CREATE TABLE IF NOT EXISTS CONSENT_AUTH_RESOURCE (
  AUTH_ID           CHAR(36) NOT NULL,
  CONSENT_ID        CHAR(36) NOT NULL,
  AUTH_TYPE         VARCHAR(255) NOT NULL,
  USER_ID           VARCHAR(255) DEFAULT NULL,
  AUTH_STATUS       VARCHAR(255) NOT NULL,
  UPDATED_TIME      BIGINT NOT NULL,
  RESOURCES         BLOB DEFAULT NULL,
  ORG_ID            VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (AUTH_ID),
  CONSTRAINT FK_CONSENT_AUTH_RESOURCE
    FOREIGN KEY (CONSENT_ID)
    REFERENCES CONSENT (CONSENT_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_auth_resource_consent_id  ON CONSENT_AUTH_RESOURCE (CONSENT_ID);
CREATE INDEX IF NOT EXISTS idx_auth_resource_user_id     ON CONSENT_AUTH_RESOURCE (USER_ID);
CREATE INDEX IF NOT EXISTS idx_auth_resource_auth_status ON CONSENT_AUTH_RESOURCE (AUTH_STATUS);
CREATE INDEX IF NOT EXISTS idx_auth_resource_org_id      ON CONSENT_AUTH_RESOURCE (ORG_ID);

-- Status audit table for tracking consent status changes
CREATE TABLE IF NOT EXISTS CONSENT_STATUS_AUDIT (
  STATUS_AUDIT_ID   CHAR(36) NOT NULL,
  CONSENT_ID        CHAR(36) NOT NULL,
  CURRENT_STATUS    VARCHAR(64) NOT NULL,
  ACTION_TIME       BIGINT NOT NULL,
  REASON            TEXT DEFAULT NULL,
  ACTION_BY         VARCHAR(255) DEFAULT NULL,
  PREVIOUS_STATUS   VARCHAR(64) DEFAULT NULL,
  ORG_ID            VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (STATUS_AUDIT_ID),
  CONSTRAINT FK_CONSENT_STATUS_AUDIT
    FOREIGN KEY (CONSENT_ID)
    REFERENCES CONSENT (CONSENT_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_status_audit_consent_id  ON CONSENT_STATUS_AUDIT (CONSENT_ID);
CREATE INDEX IF NOT EXISTS idx_status_audit_action_time ON CONSENT_STATUS_AUDIT (ACTION_TIME);

-- Full amendment history table for pre-mutation consent snapshots
CREATE TABLE IF NOT EXISTS CONSENT_HISTORY (
  HISTORY_ID       CHAR(36)     NOT NULL,
  CONSENT_ID       CHAR(36)     NOT NULL,
  ORG_ID           VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  ACTION_TIME      BIGINT       NOT NULL,
  ACTION_BY        VARCHAR(255) DEFAULT NULL,
  REASON           VARCHAR(255) DEFAULT NULL,
  SNAPSHOT         TEXT         NOT NULL,

  PRIMARY KEY (HISTORY_ID),

  CONSTRAINT FK_CONSENT_HISTORY
    FOREIGN KEY (CONSENT_ID)
    REFERENCES CONSENT (CONSENT_ID)
    ON DELETE CASCADE
);
CREATE INDEX IF NOT EXISTS idx_history_consent_time ON CONSENT_HISTORY (CONSENT_ID, ACTION_TIME);
CREATE INDEX IF NOT EXISTS idx_history_org_id       ON CONSENT_HISTORY (ORG_ID);

-- Consent attributes table for key-value pairs
CREATE TABLE IF NOT EXISTS CONSENT_ATTRIBUTE (
  CONSENT_ID        CHAR(36) NOT NULL,
  ATT_KEY           VARCHAR(255) NOT NULL,
  ATT_VALUE         VARCHAR(1024) NOT NULL,
  ORG_ID            VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (CONSENT_ID, ATT_KEY),
  CONSTRAINT FK_CONSENT_ATTRIBUTE
    FOREIGN KEY (CONSENT_ID)
    REFERENCES CONSENT (CONSENT_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_consent_attribute_att_key ON CONSENT_ATTRIBUTE (ATT_KEY);
CREATE INDEX IF NOT EXISTS idx_consent_attribute_org_id  ON CONSENT_ATTRIBUTE (ORG_ID);

-- Consent element table — each row is a specific version of an element.
-- ID groups all versions of the same element (application-assigned UUID, never reused).
-- NAME+NAMESPACE uniqueness per org is enforced at the application layer;
-- C2 provides a partial DB-level guard at the version granularity.
CREATE TABLE IF NOT EXISTS ELEMENT (
  VERSION_ID     CHAR(36) NOT NULL,
  ID             CHAR(36) NOT NULL,
  NAME           VARCHAR(255) NOT NULL,
  NAMESPACE      VARCHAR(255) NOT NULL DEFAULT 'default',
  TYPE           VARCHAR(64) NOT NULL DEFAULT 'basic',
  VERSION        INTEGER NOT NULL,
  DISPLAY_NAME   VARCHAR(255) DEFAULT NULL,
  DESCRIPTION    VARCHAR(1024) DEFAULT NULL,
  ELEMENT_SCHEMA TEXT DEFAULT NULL,
  CREATED_TIME   BIGINT NOT NULL,
  ORG_ID         VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (VERSION_ID),
  CONSTRAINT uk_element_id_version      UNIQUE (ORG_ID, ID, VERSION),
  CONSTRAINT uk_element_name_ns_version UNIQUE (ORG_ID, NAME, NAMESPACE, VERSION)
);

CREATE INDEX IF NOT EXISTS idx_element_id        ON ELEMENT (ID);
CREATE INDEX IF NOT EXISTS idx_element_name      ON ELEMENT (NAME);
CREATE INDEX IF NOT EXISTS idx_element_namespace ON ELEMENT (NAMESPACE);
CREATE INDEX IF NOT EXISTS idx_element_type      ON ELEMENT (TYPE);
CREATE INDEX IF NOT EXISTS idx_element_org_id    ON ELEMENT (ORG_ID);

-- Properties for element versions (key/value pairs scoped to element version)
CREATE TABLE IF NOT EXISTS ELEMENT_PROPERTY (
  ELEMENT_VERSION_ID  CHAR(36) NOT NULL,
  ATT_KEY             VARCHAR(255) NOT NULL,
  ATT_VALUE           VARCHAR(1024) NOT NULL,
  ORG_ID              VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (ELEMENT_VERSION_ID, ATT_KEY),
  CONSTRAINT FK_ELEMENT_PROPERTY
    FOREIGN KEY (ELEMENT_VERSION_ID)
    REFERENCES ELEMENT (VERSION_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_element_property_att_key ON ELEMENT_PROPERTY (ATT_KEY);
CREATE INDEX IF NOT EXISTS idx_element_property_org_id  ON ELEMENT_PROPERTY (ORG_ID);

-- Purpose table — each row is a specific version of a purpose.
-- ID groups all versions of the same purpose (application-assigned UUID, never reused).
-- NAME+GROUP_ID uniqueness per org is enforced at the application layer;
-- C2 provides a partial DB-level guard at the version granularity.
CREATE TABLE IF NOT EXISTS PURPOSE (
  VERSION_ID     CHAR(36) NOT NULL,
  ID             CHAR(36) NOT NULL,
  NAME           VARCHAR(255) NOT NULL,
  GROUP_ID       VARCHAR(255) NOT NULL,
  VERSION        INTEGER NOT NULL,
  DISPLAY_NAME   VARCHAR(255) DEFAULT NULL,
  DESCRIPTION    VARCHAR(1024) DEFAULT NULL,
  CREATED_TIME   BIGINT NOT NULL,
  ORG_ID         VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (VERSION_ID),
  CONSTRAINT uk_purpose_id_version         UNIQUE (ORG_ID, ID, VERSION),
  CONSTRAINT uk_purpose_name_group_version UNIQUE (ORG_ID, NAME, GROUP_ID, VERSION)
);

CREATE INDEX IF NOT EXISTS idx_purpose_id        ON PURPOSE (ID);
CREATE INDEX IF NOT EXISTS idx_purpose_name      ON PURPOSE (NAME);
CREATE INDEX IF NOT EXISTS idx_purpose_group_id  ON PURPOSE (GROUP_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_org_id    ON PURPOSE (ORG_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_org_group ON PURPOSE (ORG_ID, GROUP_ID);

-- Properties for purpose versions (key/value pairs scoped to purpose version)
CREATE TABLE IF NOT EXISTS PURPOSE_PROPERTY (
  PURPOSE_VERSION_ID  CHAR(36) NOT NULL,
  ATT_KEY             VARCHAR(255) NOT NULL,
  ATT_VALUE           VARCHAR(1024) NOT NULL,
  ORG_ID              VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (PURPOSE_VERSION_ID, ATT_KEY),
  CONSTRAINT FK_PURPOSE_PROPERTY
    FOREIGN KEY (PURPOSE_VERSION_ID)
    REFERENCES PURPOSE (VERSION_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_purpose_property_att_key ON PURPOSE_PROPERTY (ATT_KEY);
CREATE INDEX IF NOT EXISTS idx_purpose_property_org_id  ON PURPOSE_PROPERTY (ORG_ID);

-- Maps element versions to purpose versions with mandatory flag (defines versioned purpose structure)
CREATE TABLE IF NOT EXISTS PURPOSE_ELEMENT_MAPPING (
  PURPOSE_VERSION_ID   CHAR(36) NOT NULL,
  ELEMENT_VERSION_ID   CHAR(36) NOT NULL,
  MANDATORY            BOOLEAN NOT NULL DEFAULT FALSE,
  ORG_ID               VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (PURPOSE_VERSION_ID, ELEMENT_VERSION_ID),
  CONSTRAINT fk_purpose_element_purpose_ver
    FOREIGN KEY (PURPOSE_VERSION_ID)
    REFERENCES PURPOSE (VERSION_ID)
    ON DELETE CASCADE,
  CONSTRAINT fk_purpose_element_element_ver
    FOREIGN KEY (ELEMENT_VERSION_ID)
    REFERENCES ELEMENT (VERSION_ID)
    ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_purpose_element_purpose_ver ON PURPOSE_ELEMENT_MAPPING (PURPOSE_VERSION_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_element_element_ver ON PURPOSE_ELEMENT_MAPPING (ELEMENT_VERSION_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_element_org_id      ON PURPOSE_ELEMENT_MAPPING (ORG_ID);

-- Maps consents to the specific purpose version they were created against
CREATE TABLE IF NOT EXISTS PURPOSE_CONSENT_MAPPING (
  CONSENT_ID         CHAR(36) NOT NULL,
  PURPOSE_VERSION_ID CHAR(36) NOT NULL,
  ORG_ID             VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (CONSENT_ID, PURPOSE_VERSION_ID),
  CONSTRAINT fk_purpose_consent_consent
    FOREIGN KEY (CONSENT_ID)
    REFERENCES CONSENT (CONSENT_ID)
    ON DELETE CASCADE,
  CONSTRAINT fk_purpose_consent_purpose_ver
    FOREIGN KEY (PURPOSE_VERSION_ID)
    REFERENCES PURPOSE (VERSION_ID)
    ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_purpose_consent_consent     ON PURPOSE_CONSENT_MAPPING (CONSENT_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_consent_purpose_ver ON PURPOSE_CONSENT_MAPPING (PURPOSE_VERSION_ID);
CREATE INDEX IF NOT EXISTS idx_purpose_consent_org_id      ON PURPOSE_CONSENT_MAPPING (ORG_ID);

-- Stores user approval status and value for each element version in a consent
CREATE TABLE IF NOT EXISTS CONSENT_ELEMENT_APPROVAL (
  CONSENT_ID          CHAR(36) NOT NULL,
  PURPOSE_VERSION_ID  CHAR(36) NOT NULL,
  ELEMENT_VERSION_ID  CHAR(36) NOT NULL,
  APPROVED            BOOLEAN NOT NULL DEFAULT FALSE,
  VALUE               BLOB DEFAULT NULL,
  ORG_ID              VARCHAR(255) NOT NULL DEFAULT 'DEFAULT_ORG',
  PRIMARY KEY (CONSENT_ID, PURPOSE_VERSION_ID, ELEMENT_VERSION_ID),
  CONSTRAINT fk_approval_purpose_element_ver
    FOREIGN KEY (PURPOSE_VERSION_ID, ELEMENT_VERSION_ID)
    REFERENCES PURPOSE_ELEMENT_MAPPING (PURPOSE_VERSION_ID, ELEMENT_VERSION_ID)
    ON DELETE RESTRICT,
  CONSTRAINT fk_approval_consent_purpose_ver
    FOREIGN KEY (CONSENT_ID, PURPOSE_VERSION_ID)
    REFERENCES PURPOSE_CONSENT_MAPPING (CONSENT_ID, PURPOSE_VERSION_ID)
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_approval_consent     ON CONSENT_ELEMENT_APPROVAL (CONSENT_ID);
CREATE INDEX IF NOT EXISTS idx_approval_purpose_ver ON CONSENT_ELEMENT_APPROVAL (PURPOSE_VERSION_ID);
CREATE INDEX IF NOT EXISTS idx_approval_element_ver ON CONSENT_ELEMENT_APPROVAL (ELEMENT_VERSION_ID);
CREATE INDEX IF NOT EXISTS idx_approval_status      ON CONSENT_ELEMENT_APPROVAL (APPROVED);
CREATE INDEX IF NOT EXISTS idx_approval_org_id      ON CONSENT_ELEMENT_APPROVAL (ORG_ID);
